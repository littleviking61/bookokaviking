    	<p>Il voyage tous les étés à travers l'Europe, tous les deux, pendant 4 à 5 mois. Au grès du vent et des envies. On finit par parler famille, enfants, projets, voyages et pleins d'autres sujets intéressants. Je suis heureux de parler un petit peu français, de partager ce petit moment d'intimité. Au point que je vais finir par m'endormir sur la banquette, faute de place et de motivation pour planter la tente à côté.</p>
  		<p>C'est ma première nuit chez quelqu'un et une première dans un camping-car.</p>
